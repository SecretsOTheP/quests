function event_say(e)
    if(e.message:findi("hail")) then
        e.other:Message("If you have need of trading your bags, satchels, and other containters here in the Bazaar, I can assist! I will convert any container you hand me into a Token that is both sellable via the Bazaar and reconvertable back into its bag form. Simply hand me any bag or bag token (one at at time) and I'll convert it into the other and hand it back to you!
	end


function event_trade(e)
        local item_lib = require("items");
        if item_lib.check_turn_in(e.self, e.trade, {item1 = Bag_to_Token[]}) then
		    e.self:Say("Conversion complete. Here you are.");
            e.other:SummonItem({item1 = Bag_to_Token[]);
		end
		
		
